     public void inserir() {
        Servico servico = new Servico();
        
        servico.setData(dataTxt.getText());
        servico.setNome(nomeTxt.getText());
        servico.setCpf(cpfTxt.getText());
        servico.setPlaca(placaTxt.getText());
        servico.setEntrada(entradaTxt.getText());
        servico.setObs(obsTxt.getText());
        servico.setEmpresa(empresaTxt.getText());
        servico.setVigilante(vigilanteTxt.getText());
        
        ServicoDAO servicoDAO = new ServicoDAO();
        
        servicoDAO.insert(servico);
        
        JOptionPane.showMessageDialog(null, "Prestador de Servico inserido com sucesso!");
        
         preencherTabela();
          limparCampos();
    }
    
     
      public void preencherTabela() {
       
        
        ServicoDAO dao = new ServicoDAO();
        try {
            ArrayList dados = new ArrayList();

            String[] Colunas = new String[]{"Cod Entrada", "Nome", "Placa do Veiculo", "Horario de Entrada", "Empresa", "Obs."};
            
            /////////////////////////////////////
            
            
            
            // erro ao pegar a data   (nao existe o campo)
            List<Servico> erro = dao.findByData(dataTxt.getText());

            for(int i = 0; i < erro.size(); i++) {

                dados.add(new Object[]{erro.get(i).getId(), erro.get(i).getNome(), erro.get(i).getPlaca(), erro.get(i).getEntrada(), erro.get(i).getEmpresa(), erro.get(i).getObs()});
            
            }

            Tabela tabela = new Tabela(dados, Colunas);
            tblServico.setModel(tabela);
            
            tblServico.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblServico.getColumnModel().getColumn(0).setResizable(false);
            
            tblServico.getColumnModel().getColumn(1).setPreferredWidth(200);
            tblServico.getColumnModel().getColumn(1).setResizable(false);
            
            tblServico.getColumnModel().getColumn(2).setPreferredWidth(150);
            tblServico.getColumnModel().getColumn(2).setResizable(false);
            
            tblServico.getColumnModel().getColumn(3).setPreferredWidth(150);
            tblServico.getColumnModel().getColumn(3).setResizable(false);
            
            tblServico.getColumnModel().getColumn(4).setPreferredWidth(180);
            tblServico.getColumnModel().getColumn(4).setResizable(false);
            
             tblServico.getColumnModel().getColumn(5).setPreferredWidth(160);
            tblServico.getColumnModel().getColumn(5).setResizable(false);
                  
            tblServico.getTableHeader().setReorderingAllowed(false);
            tblServico.setAutoResizeMode(tblServico.AUTO_RESIZE_OFF);
            tblServico.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        }catch(Exception ex) {
            ex.printStackTrace();
        } 
    } 
     
     public void nomePrestador(String cpf) {
        
          
        PrestadorServ servico = null;
     
        PrestadorServDAO presDAO = new PrestadorServDAO();
        List<PrestadorServ> pres = presDAO.findByCpf(cpf);
        servico = pres.get(0);
        if(servico != null) 
        nomeTxt.setText(servico.getNome()); 
        
     }
     
    
     
